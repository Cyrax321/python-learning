light = "green"

if (light == "green"):
    print("Go")

elif (light == "red"):
    print("Stop")

else: 
    print("Slow down") 
print("End of program")


# Output:
# Go

# else can only be written once in an if-elif-else statement.

# In this example, the light is green, so the output is "Go".     
# If the light was red, the output would be "Stop".

# If the light was any other color, the output would be "Slow down".

# The else statement is optional. If it is not included, the program will not output anything if the conditions are not met.